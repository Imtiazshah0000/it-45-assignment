// creating function with parameters which retrun a values in string 
function citycountry(city, country) {
    return "".concat(city, ",").concat(country);
}
// calling a function and print the retruned value
console.log(citycountry("karachi", "pakistan"));
console.log(citycountry("tokyo", "japan"));
console.log(citycountry("berlin", "germant"));
